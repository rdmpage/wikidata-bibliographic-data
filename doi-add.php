foreach ($dois as $doi)
{
	$doi = strtolower($doi);
	
	//echo $doi . "\n"
	
	$go = true;
	
	$item = wikidata_item_from_doi($doi);
	
	if ($item != '')
	{
		if (!$update)
		{
			//echo "Have item already $item with $doi\n";
			
			$go = false;
		}
	}
	if ($go)
	{
		$crossref = true;
		
		if (preg_match('/10.1071\/MR/i', $doi))
		{
			$crossref = false;
		}
	
		if ($crossref)
		{
			// check cache
			
			$id = 'https://doi.org/' . $doi;
	
			$exists = $couch->exists($id);
			
			if ($exists)
			{
				$resp = $couch->send("GET", "/" . $config['couchdb_options']['database'] . "/" . urlencode($id));			
				//echo $resp;
				$work = json_decode($resp);
			}
			else
			{
				$work = get_work($doi);
				
				if ($work)
				{
					$work->_id = $id;
				
					// store
					$couch->add_update_or_delete_document($work, $work->_id, 'add');	
				}

			}
			
			if ($work)
			{
				$work->message->DOIAgency = 'crossref';
			}
			
		}
		else
		{
			$json = get('http://localhost/~rpage/microcitation/www/citeproc-api.php?guid=' . urlencode($doi));
						
			$obj = json_decode($json);
			$work = new stdclass;
			$work->message = $obj;		
		}
	
		//print_r($work);
	
		if ($work)
		{
			$source = array();
						
			
			if ($crossref)
			{
				$source[] = 'S248';
				$source[] = 'Q5188229';
								
				if (preg_match('/[\[|<|;]/', $doi))
				{
					// Some DOIs (such as BioOne SICIs) break Quickstatements
					// so we don't add these as the source
				}
				else
				{				
					// DOI seems fine, so be explict about source of data
					$source[] = 'S854';
					$source[] = '"https://api.crossref.org/v1/works/' . $doi . '"';
				}
			}
			
			
		
			$q = csljson_to_wikidata(
				$work, 
				$check, 	// check if already exists
				$update, // true to update an existing record, false to skip an existing record
				$detect_languages,
				$source,
				true // create English language label
				);
		
			echo $q;
			echo "\n";
		}
	}	


}


